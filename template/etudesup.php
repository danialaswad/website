				<h2><strong>Journ&eacute;e d'Accueil des Etudiants</strong></h2>
				<p><a href="https://www.facebook.com/media/set/?set=a.667756676576032.1073741829.140309012654137&amp;type=3" target="_blank"><img src="../cover/jae2013.jpg" alt="journee d'accueil des etudiants" class="act-image"/></a><br><br>
		Le Jeudi 24 octobre 2013, la F&eacute;d&eacute;ration des Associations et Corporations Étudiantes du 06 a organis&eacute; pour la cinqui&egrave;me fois 
		la Journ&eacute;e d’Accueil des Étudiants (JAE), en partenariat avec la Ville de Nice, le CROUS de Nice-Toulon, la Région PACA et l’Universit&eacute; Nice – Sophia Antipolis.
		Durant toute une journ&eacute;e, tous les &eacute;tudiants primo-entrants à l’universit&eacute;, en BTS ou en &eacute;cole priv&eacute;e, ont d&eacute;fendu 
		les couleurs de leurs campus lors de cette journée entièrement banalisée.<br><br>

		La journ&eacute;e s'est d&eacute;roul&eacute;e en trois parties :<br><br>

<strong><em>Le matin - Le Rallye</em></strong><br>
C’est à partir de 9 heures que, sur le Campus St Jean d’Angély, les &eacute;tudiants se sont retrouvés pour se préparer et former les équipes. Bleu, rouge, jaune et vert, les campus portaient leurs couleurs pour s’affronter tout au long de la journée.
Le petit-déjeuner était également servi, et était offert par le CROUS Nice-Toulon.
Après animations, discours, « clapping », le top départ pour la course a été donné à 10h. Les &eacute;tudiants sont alors partis à l’aventure dans la ville de Nice en suivant un parcours rythmé d’énigmes pour enfin arriver au sommet du château.<br><br>

<strong><em>L’après-midi - Challenge Inter-campus</em></strong><br>
Les étudiants ont eu rendez-vous à partir de 14h au Palais des Expositions pour représenter leur campus. Campus contre campus, chacun s’affronte dans les structures gonflables pour faire gagner son équipe. Sur place, un village composé des diff&eacute;rents partenaires de la journée était présent. À la fin de l’après-midi seul un campus est sorti vainqueur.<br><br>

<strong><em>Le soir : La Soirée responsable</em></strong><br>
A 22 heures, le Palais des Expositions a fait tomber le rideau et est devenu la plus grosse discothèque de Nice. Tous les étudiants de l’Université et des Grandes Ecoles étaient conviés, 5 000 étudiants étaient attendus pour la plus grande soirée de l’année. La consommation d’alcool était limitée au nombre de trois par personne.
