<strong><em>Le matin - Le Rallye</em></strong><br>
C’est à partir de 9 heures que, sur le Campus St Jean d’Angély, les &eacute;tudiants se sont retrouvés pour se préparer et former les équipes. Bleu, rouge, jaune et vert, les campus portaient leurs couleurs pour s’affronter tout au long de la journée.
Le petit-déjeuner était également servi, et était offert par le CROUS Nice-Toulon.
Après animations, discours, « clapping », le top départ pour la course a été donné à 10h. Les &eacute;tudiants sont alors partis à l’aventure dans la ville de Nice en suivant un parcours rythmé d’énigmes pour enfin arriver au sommet du château.<br><br>

<strong><em>L’après-midi - Challenge Inter-campus</em></strong><br>
Les étudiants ont eu rendez-vous à partir de 14h au Palais des Expositions pour représenter leur campus. Campus contre campus, chacun s’affronte dans les structures gonflables pour faire gagner son équipe. Sur place, un village composé des diff&eacute;rents partenaires de la journée était présent. À la fin de l’après-midi seul un campus est sorti vainqueur.<br><br>

<strong><em>Le soir : La Soirée responsable</em></strong><br>
A 22 heures, le Palais des Expositions a fait tomber le rideau et est devenu la plus grosse discothèque de Nice. Tous les étudiants de l’Université et des Grandes Ecoles étaient conviés, 5 000 étudiants étaient attendus pour la plus grande soirée de l’année. La consommation d’alcool était limitée au nombre de trois par personne.<br><br>
