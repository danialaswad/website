	<br/><br>
	4 mois de programme d’int&eacute;gration dit « Bridging Programme ». De mi-avril à fin ao&ucirc;t, les étudiants sont r&eacute;partis en France dans les 4 villes des IUT concern&eacute;s 
	pour une p&eacute;riode d’int&eacute;gration durant laquelle ils sont h&eacute;berg&eacute;s en familles d’accueil, puis en r&eacute;sidence Universitaire. Ils re&ccedil;oivent un enseignement intensif
